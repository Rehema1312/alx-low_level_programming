/**
 * _islower - returns 1 for lowercase alphabet and 0 for uppercase
 * @c: taking in a character
 * Return: 1 for lowercase and 0 for uppercase
 */
int _islower(int c)
{
		if (c >= 'a' && c <= 'z')
				{
							return (1);
								}
			else
					{
								return (0);
									}
}
